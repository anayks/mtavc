						if((mug >= 315 and mug <= 360) or ((mug <= 45 and mug >= 0) or (mug >= 315 and mug <= 360)) or (mug >= 135 and mug <= 225)) then
							if(nug >= 330) then
								peremka = nug + 30 - 360
