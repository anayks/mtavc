						local ug = math.atan2(x, y)
						local mug = math.deg(ug)
						if(mug <= 0) then
							mug = 360 + mug
						end
						mug = 360 - mug
						local id = getElementData(source, "ID")
						local nug = svtable[tonumber(id)]["rz"]
